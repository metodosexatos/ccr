# Operações matemáticas:

5 + 3      # operação de adição
5 - 3      # operação de subtração
5 * 3      # operação de multiplicação
5 / 3      # operação de divisão
5 ** 3     # operação de potenciação
sqrt(3)    # operação de radiciação
5 %% 3     # módulo da divisão
5 %/% 3    # quociente da divisão
log(3,5)   # logaritmo

# Operações matemáticas - script otimizado:

a <- 70     # a variável (objeto) a vai receber o número a direita do simbolo "<-".
b <- 90     # a variável (objeto) b vai receber o número a direita do simbolo "<-". 

a + b      # operação de adição
a - b      # operação de subtração
a * b      # operação de multiplicação
a / b      # operação de divisão
a ** b     # operação de potenciação
sqrt(b)    # operação de radiciação
a %% b     # módulo da divisão
a %/% b    # quociente da divisão
log(b,a)   # logaritmo









